#Parse a String to a Float or Integer

st="123456789"

print(st)
print(type(st))
#integer
int_st=int(st)
print(int_st)
print(type(int_st))
print("\n")
#float
st="125.36"
print(st)
print(type(st))

f_st=float(st)
print(f_st)
print(type(f_st))
print("\n")

#float to integer

s="156.96"
f_s=float(s)
print(f_s)
print(type(f_s))

int_f=int(f_s)
print(int_f)
print(type(int_f))